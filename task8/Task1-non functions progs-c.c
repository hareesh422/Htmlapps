//Addition of Number by using Non-Functional Statements-C Langauge
#include <stdio.h>
int main()
{
  int n1,n2,result;
  n1=60;
  n2=120;
  result=n1+n2;
  printf("%d\n",result);
  n1=90;
  n2=70;
  result=n1+n2;
  printf("%d\n",result);
}
